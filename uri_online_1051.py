'''
   0.00 - 2000.00  = 0
2000.01 - 3000.00  = 8%
3000.01 - 4500.00  = 18%
 acima de 4500.00  = 28%

'''

s = float(input())

i = 0
if s <= 2000.00:
    print('Isento')
else:
    if s > 4500:
        s -= 4500
        i += s*0.28
        s = 4500
    if 3000 < s <= 4500:
        s -= 3000
        i += s * 0.18
        s = 3000
    if 2000 < s <= 3000:
        s -= 2000
        i += s * 0.08
        s = 2000
    print('R$ %.2f' %i)



